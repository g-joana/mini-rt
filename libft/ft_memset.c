#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*dest;

	i = 0;
	dest = (char *) s;
	while (i < n)
	{
		dest[i] = c;
		i++;
	}
	return (dest);
}
